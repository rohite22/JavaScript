//if Else IF statements
let age = 25

if(age > 18) //expression1
{
    console.log('You are eligible for voting') //statement to be executed if expression1 is true
}
else if(age == 18) //expression2
{
    console.log('great, you are eligible to vote') //statement to be executed if expression2 is true
}

else
{
    console.log('wait until you becomes 18') //statement to be executed if both the expressions are false
}