//Bitwise operators treat arguments as 32 bit numbers and work on the level of their inary representations.

console.log('****************Bitwise Operator************')
let A = 2
let B = 5
console.log(A & B) //logical AND & to compare binry of both the variables and returns the output after calculations
console.log(A | B) //logical OR | returns the output after calculating and coverting the binary of both the variable values to decimal.
console.log(A^B) //Sets each bit to 1 if only one bit of the two bits is 1.
console.log(~7) //inverts all te bits
console.log(5<<1) //shifts left by 1 and empty space filled with 0
console.log(4>>2) //shift right by 2